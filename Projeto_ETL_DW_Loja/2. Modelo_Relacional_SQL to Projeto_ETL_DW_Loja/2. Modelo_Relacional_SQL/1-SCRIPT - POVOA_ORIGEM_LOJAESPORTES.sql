CREATE DATABASE LOJAESPORTE;

USE LOJAESPORTE;

CREATE TABLE CATEGORIAPRODUTO
(
  CODCATEGORIAPRODUTO INTEGER not null,
  DESCATEGORIAPRODUTO VARCHAR(40)
)
;
ALTER TABLE CATEGORIAPRODUTO
  ADD CONSTRAINT PK_CATEGORIAPRODUTO PRIMARY KEY (CODCATEGORIAPRODUTO);


CREATE TABLE TIPOCLIENTE
(
  CODTIPOCLIENTE INTEGER not null,
  DESTIPOCLIENTE VARCHAR(6)
)
;
ALTER TABLE TIPOCLIENTE
  ADD CONSTRAINT PK_TIPOCLIENTE PRIMARY KEY (CODTIPOCLIENTE);

CREATE TABLE CLIENTE
(
  CODCLIENTE       INTEGER not null,
  NOMECLIENTE      VARCHAR(40),
  EMAILCLIENTE     VARCHAR(50),
  TELEFONECLIENTE  VARCHAR(12),
  DESCRICAOCLIENTE VARCHAR(300),
  CEPCLIENTE       VARCHAR(9),
  RUA              VARCHAR(30),
  NUMEROEND        INTEGER,
  COMPLEMENTOEND   VARCHAR(20),
  CIDADECLIENTE    VARCHAR(40),
  CODESTADO        VARCHAR(2),
  CODTIPOCLIENTE   INTEGER NOT NULL
)
;
ALTER TABLE CLIENTE
  ADD CONSTRAINT PK_CLIENTE PRIMARY KEY (CODCLIENTE);
ALTER TABLE CLIENTE
  ADD CONSTRAINT FK_TIPOCLIENTE_CLIENTE FOREIGN KEY (CODTIPOCLIENTE)
  REFERENCES TIPOCLIENTE (CODTIPOCLIENTE);


CREATE TABLE FORMAPAGAMENTO
(
  CODFORMAPAGAMENTO INTEGER not null,
  DESFORMAPAGAMENTO VARCHAR(50)
)
;
ALTER TABLE FORMAPAGAMENTO
  ADD CONSTRAINT PK_FORMAPAGAMENTO PRIMARY KEY (CODFORMAPAGAMENTO);


CREATE TABLE PEDIDO
(
  CODPEDIDO   INTEGER not null,
  DATAPEDIDO  DATE,
  
  CODCLIENTE  INTEGER
)
;
ALTER TABLE PEDIDO
  ADD CONSTRAINT PK_PEDIDO PRIMARY KEY (CODPEDIDO);
ALTER TABLE PEDIDO
  ADD CONSTRAINT FK_CLIENTE_PEDIDO FOREIGN KEY (CODCLIENTE)
  REFERENCES CLIENTE (CODCLIENTE);


CREATE TABLE PRODUTO
(
  CODPRODUTO          INTEGER not null,
  NOMEPRODUTO         VARCHAR(40),
  PRECOPRODUTO        NUMERIC(10,5),
  CODCATEGORIAPRODUTO INTEGER
)
;
ALTER TABLE PRODUTO
  ADD CONSTRAINT PK_PRODUTO PRIMARY KEY (CODPRODUTO);
ALTER TABLE PRODUTO
  ADD CONSTRAINT FK_CATEGORIAPRODUTO_PRODUTO FOREIGN KEY (CODCATEGORIAPRODUTO)
  REFERENCES CATEGORIAPRODUTO (CODCATEGORIAPRODUTO);


CREATE TABLE ITEM
(
  CODPEDIDO         INTEGER not null,
  CODPRODUTO        INTEGER not null,
  QUANTIDADEITEM    INTEGER,
  VALORUNITARIOITEM NUMERIC(10,5)
)
;
ALTER TABLE ITEM
  ADD CONSTRAINT PK_ITEM PRIMARY KEY (CODPEDIDO, CODPRODUTO);
ALTER TABLE ITEM
  ADD CONSTRAINT FK_PEDIDO_ITEM FOREIGN KEY (CODPEDIDO)
  REFERENCES PEDIDO (CODPEDIDO);
ALTER TABLE ITEM
  ADD CONSTRAINT FK_PRODUTO_ITEM FOREIGN KEY (CODPRODUTO)
  REFERENCES PRODUTO (CODPRODUTO);


CREATE TABLE PAGAMENTO
(
  CODPEDIDO         INTEGER not null,
  CODFORMAPAGAMENTO INTEGER not null,
  DATAPAGAMENTO     DATE,
  VALORPAGAMENTO    NUMERIC(10,5)
)
;
ALTER TABLE PAGAMENTO
  ADD CONSTRAINT PK_PAGAMENTO PRIMARY KEY (CODPEDIDO, CODFORMAPAGAMENTO);
ALTER TABLE PAGAMENTO
  ADD CONSTRAINT FK_FORMAPAGAMENTO_PAGAMENTO FOREIGN KEY (CODFORMAPAGAMENTO)
  REFERENCES FORMAPAGAMENTO (CODFORMAPAGAMENTO);
ALTER TABLE PAGAMENTO
  ADD CONSTRAINT FK_PEDIDO_PAGAMENTO FOREIGN KEY (CODPEDIDO)
  REFERENCES PEDIDO (CODPEDIDO);


DELETE FROM PAGAMENTO;

DELETE FROM ITEM;

DELETE FROM PRODUTO;

